package src.company.collection.set;

import java.util.HashSet;

public class HashSetSample {
    public static void main(String[] args) {
        HashSet<String> hashSet = new HashSet<>();
        hashSet.add("1324");
        hashSet.add("1323");
        hashSet.add("131131");
        hashSet.add("13");
        System.out.println("hashSet = " + hashSet);
    }
}
